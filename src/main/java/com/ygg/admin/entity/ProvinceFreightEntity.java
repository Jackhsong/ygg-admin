package com.ygg.admin.entity;

public class ProvinceFreightEntity
{
    private int id;
    
    private int freightTemplateId;
    
    private int provinceId;
    
    private int freightMoney;
    
    public int getId()
    {
        return id;
    }
    
    public void setId(int id)
    {
        this.id = id;
    }
    
    public int getFreightTemplateId()
    {
        return freightTemplateId;
    }
    
    public void setFreightTemplateId(int freightTemplateId)
    {
        this.freightTemplateId = freightTemplateId;
    }
    
    public int getProvinceId()
    {
        return provinceId;
    }
    
    public void setProvinceId(int provinceId)
    {
        this.provinceId = provinceId;
    }
    
    public int getFreightMoney()
    {
        return freightMoney;
    }
    
    public void setFreightMoney(int freightMoney)
    {
        this.freightMoney = freightMoney;
    }
    
}
